#include "main.h"
/**
 * jack_bauer - jack_bauer block
 *
 * Descripton:prints every minute starting from 00:00 to 23:59
 *
 * Return: 0
 */
void jack_bauer(void)
{
	int h, m;

	for (h = 0; h < 24; h++)
	{
		for (m = 0; m < 60; m++)
		{
			_putchar('0' + h / 10);
			_putchar('0' + h % 10);
			_putchar(':');
			_putchar('0' + m / 10);
			_putchar('0' + m % 10);
			_putchar('\n');
		}
	}
}
